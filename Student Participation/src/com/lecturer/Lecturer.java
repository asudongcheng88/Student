package com.lecturer;

public class Lecturer {
	
	private String lecId;
	private String lecName;
	private String lecEmail;
	private String lecPass;
	public String getLecId() {
		return lecId;
	}
	public void setLecId(String lecId) {
		this.lecId = lecId;
	}
	public String getLecName() {
		return lecName;
	}
	public void setLecName(String lecName) {
		this.lecName = lecName;
	}
	public String getLecEmail() {
		return lecEmail;
	}
	public void setLecEmail(String lecEmail) {
		this.lecEmail = lecEmail;
	}
	public String getLecPass() {
		return lecPass;
	}
	public void setLecPass(String lecPass) {
		this.lecPass = lecPass;
	}
	public Lecturer() {
		super();
	}
	
	

}
